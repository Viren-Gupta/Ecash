package PackageOne;

class Sample_pos{
	private String item;
	private double cost;
	public Sample_pos(String item, double cost) {
		this.item = item;
		this.cost = cost;
	}
	public String getItem() {
		return item;
	}
	public void setItem(String item) {
		this.item = item;
	}
	public double getCost() {
		return cost;
	}
	public void setCost(double cost) {
		this.cost = cost;
	}
	
}
